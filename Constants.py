import numpy as np

N = 0
M = 0
Pi = []
A = []
B = []


def setN(n):
    global N
    N = n



def setM(m):
    global M
    M = m


def setA(a):
    global A
    A = np.array(a, dtype=np.longdouble)


def setB(b):
    global B
    B = np.array(b, dtype=np.longdouble)


def setPi(pi):
    global Pi
    Pi = np.array(pi, dtype=np.longdouble)


